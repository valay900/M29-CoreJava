// Program to demonstrate on various data types
package org.tnsif.introduction;

public class DataType {

	public static void main(String[] args) {
		
		// integer will give us the values in whole number
		int a = 10/2;
		// float and double will gives us the value in decimal point
		float b = 10/2;
		// if we use d after any number it gives precision upto 16 decimal places
		double c = 10/3d;
		// if we use f after any number it gives precision upto 7 decimal places
		float d = 10/3f;
		
		
		System.out.println("The value is " + a);
		System.out.println("The value is " + b);
		System.out.println("The value is " + c);
		System.out.println("The value is " + d);
		
	}

}